import re  # импортируем модуль регулярных выражений для проверки имени файла по шаблону
from pathlib import Path  # импортируем класс Path для работы с путями к файлам и каталогам
import random  # импортируем модуль для псевдослучайного выбора символов при генерации содержимого
import string  # импортируем наборы стандартных символьных констант (буквы, цифры, знаки)

from core.config import (  # импортируем константы конфигурации, используемые файловым менеджером
    USER_UPDATES_DIR,  # каталог пользовательских файлов обновлений
    USER_SIGNATURES_DIR,  # каталог пользовательских файлов подписей
    MAX_USER_FILES,  # максимальное допустимое количество пользовательских файлов
    MAX_FILENAME_LENGTH,  # максимальная допустимая длина имени файла
    MAX_USER_FILE_SIZE_BYTES,  # максимальный допустимый размер файла в байтах
    MAX_FILE_CONTENT_LENGTH,  # максимальная допустимая длина текстового содержимого
)
from core.utils import (  # импортируем вспомогательные функции общего назначения
    get_files_from_directory,  # функция получения списка файлов из каталога
    build_signature_path,  # функция построения пути к соответствующему файлу подписи
)


def get_user_files():  # функция получения списка пользовательских файлов обновлений
    return get_files_from_directory(USER_UPDATES_DIR)  # возвращаем список файлов из пользовательского каталога обновлений


def normalize_filename(filename: str, default_extension=".txt"):  # функция нормализации имени файла
    filename = filename.strip()  # удаляем пробелы в начале и в конце введённого имени файла

    if not filename:  # проверяем, осталось ли имя файла непустым после удаления пробелов
        return filename  # возвращаем пустое значение без дальнейшей обработки

    if not filename.endswith(default_extension):  # проверяем, оканчивается ли имя файла на стандартное расширение
        filename += default_extension  # если расширения нет, автоматически добавляем ".txt"

    return filename  # возвращаем нормализованное имя файла


def validate_filename(filename: str):  # функция проверки корректности имени файла
    if not filename:  # проверяем, передано ли непустое имя файла
        return False, "Имя файла не может быть пустым."  # возвращаем признак ошибки и поясняющее сообщение

    if filename.endswith(".txt"):  # проверяем, содержит ли имя стандартное расширение текстового файла
        base_name = filename[:-4]  # отделяем основную часть имени без расширения ".txt"
    else:
        base_name = filename  # если расширения нет, используем всё имя целиком

    if not base_name:  # проверяем, не оказалось ли имя пустым после удаления расширения
        return False, "Имя файла не может быть пустым."  # возвращаем сообщение об ошибке

    if len(filename) > MAX_FILENAME_LENGTH:  # проверяем, не превышает ли имя файла максимально допустимую длину
        return False, f"Имя файла не должно превышать {MAX_FILENAME_LENGTH} символов."  # возвращаем сообщение о превышении длины

    if not re.fullmatch(r"[A-Za-z0-9_-]+", base_name):  # проверяем имя по шаблону допустимых символов
        return False, (
            "Имя файла может содержать только латинские буквы, цифры, символы '_' и '-'."
        )  # возвращаем сообщение о недопустимых символах

    return True, ""  # возвращаем успешный результат проверки и пустое сообщение об ошибке


def validate_content(content: str):  # функция проверки допустимой длины текстового содержимого файла
    if len(content) > MAX_FILE_CONTENT_LENGTH:  # проверяем, не превышает ли текст максимально допустимую длину
        return False, (
            f"Размер содержимого превышает допустимый предел "
            f"({MAX_FILE_CONTENT_LENGTH} символов)."
        )  # возвращаем сообщение о превышении лимита

    return True, ""  # возвращаем успешный результат проверки и пустое сообщение


def create_file_with_content(file_path: Path, content: str):  # функция создания файла с заданным текстовым содержимым
    encoded_content = content.encode("utf-8")  # кодируем текст в UTF-8 для проверки реального размера в байтах

    if len(encoded_content) > MAX_USER_FILE_SIZE_BYTES:  # проверяем, не превышает ли размер файла допустимый предел
        raise ValueError(
            f"Размер файла превышает допустимый предел ({MAX_USER_FILE_SIZE_BYTES} байт)."
        )  # возбуждаем исключение при превышении допустимого размера

    file_path.write_text(content, encoding="utf-8")  # записываем текстовое содержимое в файл в кодировке UTF-8


def generate_random_content(length=200):  # функция генерации случайного содержимого файла
    characters = string.ascii_letters + string.digits + string.punctuation + " \n"  # формируем набор допустимых символов для генерации
    return "".join(random.choice(characters) for _ in range(length))  # создаём строку заданной длины случайным выбором символов